    public All_Students_Result(int Comp_Id) {
        initComponents();
        Cur_CompId=Comp_Id;
         try
        {
            Connection con=ConnectionProvider.getcon();
            PreparedStatement st=con.prepareStatement("select * from Competition where Comp_Id=?");
            st.setInt(1, Cur_CompId);
            ResultSet rs=st.executeQuery();
            if(rs.first())
            {
                jLabel3.setText(Integer.toString(rs.getInt(1)));
                jLabel5.setText(rs.getString(3));
                jLabel7.setText(rs.getString(4));
            }
            PreparedStatement st1=con.prepareStatement("select Candidate.Candidate_Id,Candidate.name,Competition.Comp_name,Result.score "
                    + "  from Candidate,result,Competition where Result.Comp_Id=? and Candidate.candidate_Id=Result.candidate_Id "
                    + "and Competition.Comp_Id=Result.Comp_ID");
            st1.setInt(1, Cur_CompId);
            
            
            ResultSet rs1=st1.executeQuery();
               
            
            jTable1.setModel(DbUtils.resultSetToTableModel(rs1));
            
           
        }
        catch (Exception e) 
        {
           JOptionPane.showMessageDialog(rootPane,e.getMessage(), "Error", 2);
        }
    }

